// https://gobyexample.com/values
package main

import "fmt"

func main() {
	// Two Strings being added together
	fmt.Println("Go" + "lang")
	// A string and an Integer result
	fmt.Println("1+1 =", 1+1)
	// Same as above, but with float
	fmt.Println("7.0/3.0 = ", 7.0/3.0)

	//Booleans
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
